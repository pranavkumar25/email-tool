// Bundles the Apps Script template into a TS module the app can push via the
// Apps Script API. Edit apps-script/* (the canonical source) then run:
//   pnpm bundle:script
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const code = readFileSync(join(root, "apps-script/Code.gs"), "utf8");
const manifest = readFileSync(join(root, "apps-script/appsscript.json"), "utf8");

const out = `// AUTO-GENERATED by scripts/bundle-apps-script.mjs — do not edit by hand.
// Edit apps-script/Code.gs + apps-script/appsscript.json, then run \`pnpm bundle:script\`.

export const APPS_SCRIPT_CODE = ${JSON.stringify(code)};

export const APPS_SCRIPT_MANIFEST = ${JSON.stringify(manifest)};
`;

mkdirSync(join(root, "src/server/google"), { recursive: true });
writeFileSync(join(root, "src/server/google/appsScriptSource.ts"), out);
console.log("Wrote src/server/google/appsScriptSource.ts");
